package Implementation;

public class numberOfTries
{

    int numbersOfTries=0;
    numberOfTries( int numbersOfTries)
    {
        this.numbersOfTries = numbersOfTries;
    }

    public  void  addNumbersOfTries()
    {
        numbersOfTries++;
    }
    public int getNumbersOfTries() {
        return numbersOfTries;
    }
}
